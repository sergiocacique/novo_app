             <div id="boxsicconteudo">
                 <div class="sobre">
                     <ul>
                         <li>Dirija-se à unidade física do SIC pertencente ao órgão ao qual você pretende solicitar a informação</li>
                         <li>Aguarde a inserção da solicitação no e-SIC e receba o seu número de protocolo. Guarde o seu número de protocolo, pois ele é o comprovante do cadastro da solicitação via sistema</li>
                     </ul>
                 </div>
             </div>
             <div id="boxsicconteudo">
                 <div class="sobre">
                     <ul>
                         <li>Acesse o e-SIC (Sistema Eletrônico do Serviço de Informações ao Cidadão):
                             http://esic.boavista.rr.gov.br</li>
                         <li>No sistema, clique em “Cadastre-se” para realizar o seu cadastro no sistema. Na própria tela de cadastro, você irá escolher seu nome de usuário e a senha de acesso</li>
                         <li>Acesse o sistema com seu nome de usuário e senha</li>
                         <li>Clique em “Registrar Pedido” e preencha o formulário de solicitação de pedido. Atenção: Antes de realizar o pedido, leia atentamente as dicas para o pedido e conheça os procedimentos que devem ser adotados para fazer sua solicitação</li>
                         <li>O e-SIC irá disponibilizar um número de protocolo e, também, o enviará por e-mail. Guarde o seu número de protocolo, pois ele é o comprovante do cadastro da solicitação via sistema</li>
                     </ul>
                 </div>
