                             <h4>SOBRE O PORTAL<br></h4>

                             <p class="text-left">Ter acesso fácil à prestação de contas referentes às ações do Executivo é um direito do cidadão e uma importante ferramenta para o exercício da democracia.
                                 <br><br>
                                 Atendendo à Lei Federal de Acesso à Informação, nº 12.527/11, a Prefeitura de Boa Vista disponibiliza o Portal da Transparência, que é um importante canal de comunicação, publicação e divulgação dos atos realizados pelo Poder Executivo Municipal.
                                 <br><br>
                                 O objetivo é aumentar a transparência da gestão pública e estimular a participação da sociedade na administração pública e no combate à corrupção.
                                 <br><br>
                                 A Prefeitura, ao apresentar seus atos e resultados, demonstra o compromisso com a cidade e os cidadãos.</p>
